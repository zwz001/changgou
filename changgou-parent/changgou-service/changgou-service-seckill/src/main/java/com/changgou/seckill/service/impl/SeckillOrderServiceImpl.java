    /**
     * 添加订单
     *
     * @param id
     * @param time
     * @param username
     * @return
     */
    @Override
    public Boolean add(Long id, String time, String username) {
        //递增，判断是否排队（获取"UserQueueCount"对应的值加1之后的值）
        Long userQueueCount = redisTemplate.boundHashOps("UserQueueCount").increment(username, 1);
        if (userQueueCount > 1) {
            //100:表示有重复抢单
            throw new RuntimeException(String.valueOf(StatusCode.REPERROR));
        }
        //避免超卖->使用分布式锁
        RLock mylock = redissonClient.getLock("Mylock");//获取锁
        try {
            //上锁
            mylock.lock(100, TimeUnit.SECONDS);
            //排队信息封装
            SeckillStatus seckillStatus = new SeckillStatus(username, new Date(), 1, id, time);
            //将秒杀抢单信息存入到redis中，这里采用List方式存储，List本身是一个队列
            redisTemplate.boundListOps("SeckillOrderQueue").leftPush(seckillStatus);
            //将抢单状态存入到redis中
            redisTemplate.boundHashOps("UserQueueStatus").put(username, seckillStatus);
        } catch (Exception e) {
            e.printStackTrace();
            return false;   //报错时直接返回false
        } finally {
            mylock.unlock();    //释放锁
        }
        //多线程操作
        multiThreadingCreateOrder.createOrder();
        return true;
    }

    @Override
    public SeckillStatus getUserSeckillStatus(String username) {
        return (SeckillStatus) redisTemplate.boundHashOps(SystemConstants.SEC_KILL_USER_STATUS_KEY).get(username);
    }
